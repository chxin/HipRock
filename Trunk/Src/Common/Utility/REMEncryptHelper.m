    return base64String;
}

#pragma mark - private methods
+ (NSData *)getVectorData
{
    NSData *data = [vector dataUsingEncoding:NSUTF8StringEncoding allowLossyConversion:YES];
    return [GTMBase64 decodeData:data];
}
@end
